import * as React from "react"
import { cva } from "class-variance-authority"
import { cn } from "../../lib/utils"

const buttonVariants = cva(
    "inline-flex items-center justify-center whitespace-nowrap rounded-lg text-sm font-medium transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-primary focus-visible:ring-offset-2 disabled:pointer-events-none disabled:opacity-50",
    {
        variants: {
            variant: {
                default: "bg-primary text-white hover:bg-primary-dark shadow-lg shadow-primary/25",
                destructive: "bg-red-500 text-white hover:bg-red-600",
                outline: "border-2 border-primary text-primary hover:bg-primary hover:text-white",
                secondary: "bg-secondary text-white hover:bg-secondary-dark shadow-lg shadow-secondary/25",
                ghost: "hover:bg-surface text-gray-300 hover:text-white",
                link: "text-primary underline-offset-4 hover:underline",
                white: "bg-white text-primary hover:bg-gray-100 shadow-lg",
            },
            size: {
                default: "h-10 px-4 py-2",
                sm: "h-9 rounded-md px-3",
                lg: "h-12 rounded-lg px-8 text-base",
                icon: "h-10 w-10",
            },
        },
        defaultVariants: {
            variant: "default",
            size: "default",
        },
    }
)

const Button = React.forwardRef(({ className, variant, size, ...props }, ref) => {
    return (
        <button
            className={cn(buttonVariants({ variant, size, className }))}
            ref={ref}
            {...props}
        />
    )
})
Button.displayName = "Button"

export { Button, buttonVariants }
